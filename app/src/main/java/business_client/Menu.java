package business_client;

public class Menu {
	
	//매장 메뉴
	
	private int menu_no;    	// 메뉴 번호          
	private String menu_name;   // 메뉴 이름         
	private int menu_price;     // 메뉴 가격    
	private String option;      // 메뉴 옵션         
	private String size;        // 사이즈       
	private int menu_point;     // 포인트      
	private int shop_no;        // 매장 번호   
	private String menu_img;    // 메뉴 이미지
	
 	public int getMenu_no() {
		return menu_no;
	}
	public void setMenu_no(int menu_no) {
		this.menu_no = menu_no;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public int getMenu_price() {
		return menu_price;
	}
	public void setMenu_price(int menu_price) {
		this.menu_price = menu_price;
	}
	public String getOption() {
		return option;
	}
	public void setOption(String option) {
		this.option = option;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getMenu_point() {
		return menu_point;
	}
	public void setMenu_point(int menu_point) {
		this.menu_point = menu_point;
	}
	public int getShop_no() {
		return shop_no;
	}
	public void setShop_no(int shop_no) {
		this.shop_no = shop_no;
	}
	public String getMenu_img() {
		return menu_img;
	}
	public void setMenu_img(String menu_img) {
		this.menu_img = menu_img;
	}
	
	
	
}
